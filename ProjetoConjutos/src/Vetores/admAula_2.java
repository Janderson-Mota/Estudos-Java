package Vetores;

public class admAula_2 {

	private String nome;
	private double preco;
	
	
	public admAula_2(String nome, double price) {
		super();
		this.nome = nome;
		this.preco = price;
	}


	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}


	
	public double getPrice() {
		return preco;
	}

	public void setPrice(double price) {
		this.preco = price;
	}
	
	
	
}
