package administrador;

public class sistemaInterno {

	public double x , y , resultado;
	
	
	public double soma () {
		return resultado = x + y ;
	}
	
	public double subtracao () {
		return resultado = x - y ;
	}
	
	public double multiplicacao () {
		return resultado = x * y ;
	}
	
	public double divisao () {
		return resultado = x / y ;
	}
	
	
}
